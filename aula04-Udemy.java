public class aula04 { // exemplo de operações numéricas
    
    public static void main(String[] args) {
     
        // criando variáveis do tipo int

        int x, y, result;

        // atribuindo valores às variáveis

        x = 6;
        y = 3;

        // realizando operações com as variáveis

        result = x+y;
        System.out.println("Soma = " + result);

        result = x-y;
        System.out.println("Subtração = " + result);

        result = x*y;
        System.out.println("Multiplicação = " + result);

        result = x/y;
        System.out.println("Divisão = " + result);

        result = x%y;
        System.out.println("Resto ou módulo = " + result);
    }
}
